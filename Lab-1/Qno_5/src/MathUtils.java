//Qno5b Static Methods

class MathUtils {
    static int add(int a, int b) {
        return a + b;
    }

    public static void main(String[] args) {
        int sum = MathUtils.add(5, 3);  // Calling static method
        System.out.println("Sum: " + sum);
        System.out.println("Lab No.: 1\n" +
                "Name: Nishchal K.C.\n");
    }
}
